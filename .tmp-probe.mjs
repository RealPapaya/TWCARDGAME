import { chromium } from "playwright";
const BASE = process.env.BASE || "http://localhost:5174";
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));
const browser = await chromium.launch();
const page = await browser.newPage({ viewport: { width: 1280, height: 800 } });
page.on("console", (m) => { const t=m.text(); if(t.includes("ERR")||m.type()==="error") console.log("PAGEERR>", t); });
await page.goto(BASE, { waitUntil: "networkidle" });
await sleep(800);
await page.screenshot({ path: ".tmp-probe-landing.png" });
const testids = await page.$$eval("[data-testid]", els => [...new Set(els.map(e=>e.getAttribute("data-testid")))].slice(0,60));
const menuScreens = await page.$$eval("[data-menu-screen]", els => els.map(e=>e.getAttribute("data-menu-screen")));
const buttons = await page.$$eval("button", els => els.map(e=>e.textContent.trim()).filter(Boolean).slice(0,40));
console.log("TESTIDS>", JSON.stringify(testids));
console.log("MENUSCREENS>", JSON.stringify(menuScreens));
console.log("BUTTONS>", JSON.stringify(buttons));
await browser.close();
